#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;
	int		i;

	i = 0;
	c = '\n';
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &c, 1);
}
