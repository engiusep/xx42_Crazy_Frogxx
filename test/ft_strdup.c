#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		size;
	char	*ptr;
	int		i;

	i = 0;
	size = ft_strlen(s);
	ptr = malloc(size * sizeof(char) + 1);
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
